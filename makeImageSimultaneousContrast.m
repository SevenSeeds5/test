function I = makeImageSimultaneousContrast(N)
    N = 256;
    I = .8*ones(N, N, 3);
    I(:,1:N/2,:) = .4* ones(N, N/2,  3);

    I(7*N/16 +1 : 9*N/16 ,3*N/16 +1 : 5*N/16,   1) =  .6*ones(N/8,N/8) ;
    I(7*N/16 +1 : 9*N/16 ,3*N/16 +1 : 5*N/16,   2) =  .6*ones(N/8,N/8) ;
    I(7*N/16 +1 : 9*N/16 ,3*N/16 +1 : 5*N/16,   3) =  .6*ones(N/8,N/8) ;

    I(7*N/16 +1 : 9*N/16 ,11*N/16 +1 : 13*N/16,   1) =  .6*ones(N/8,N/8) ;
    I(7*N/16 +1 : 9*N/16 ,11*N/16 +1 : 13*N/16,   2) =  .6*ones(N/8,N/8) ;
    I(7*N/16 +1 : 9*N/16 ,11*N/16 +1 : 13*N/16,   3) =  .6*ones(N/8,N/8) ;

